def add(a,b):
    """
    Retourne l'addition de deux nombres
    :param a: Premier nombre
    :param b: Deuxième nombre
    :return: L'addition des deux nombres
    """
    return a + b

def subtract(a,b):
    """
    Cette fonction permet de soustraire un nombre b à un nombre a
    :param a: Premier nombre
    :param b: Deuxième nombre
    :return: La soustraction des deux nombres
    """
    return b - a

def multiply(a,b):
    """
    Cette fonction permet de multiplier deux nombres
    :param a: Premier nombre
    :param b: Deuxième nombre
    :return: La multiplication de deux nombres
    """
    return a * b

def divide(a,b):
    """
    Cette fonction permet de diviser un nombre a à un nombre b
    :param a: Premier nombre
    :param b: Deuxième nombre
    :return: La division des deux nombres
    """
    # TODO: uniformiser en mettant b / a et aussi check pour si on veut faire division entière ou pas vu qu'on a des floats
    return a // b
